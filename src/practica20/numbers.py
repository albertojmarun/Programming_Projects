print("Introduce the first number")
first_number = int(input())

print("Introduce the second number")
second_number = int(input())

print("Numeros:")
print(str(first_number), " y ", str(second_number))

print("Suma (", str(first_number), " + ", str(second_number), ") = ", str(first_number +  second_number), "\n")

print("Resta (", str(first_number), " - ", str(second_number), ") = ", str(first_number -  second_number), "\n")

print("Multiplicacion (", str(first_number), " * ", str(second_number), ") = ", str(first_number *  second_number), "\n")

print("Division (", str(first_number), " / ", str(second_number), ") = ", str(first_number /  second_number), "\n")